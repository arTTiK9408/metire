class TimerCounter extends StatelessComponent {
  final int seconds;
  final Color color;
  const TimerCounter({required this.seconds, required this.color, super.key});

  String get _display {
    final mins = seconds ~/ 60;
    final secs = seconds % 60;
    return '${mins.toString().padLeft(2, '0')}:${secs.toString().padLeft(2, '0')}';
  }

  List<String> _buildLines(Map<String, List<String>> font) {
    final height = font['0']!.length;
    final lines = List.filled(height, '');
    for (final char in _display.split('')) {
      final digit = font[char] ?? font[' ']!;
      for (int i = 0; i < height; i++) {
        lines[i] += '${digit[i]} ';
      }
    }
    return lines;
  }

  @override
  Component build(BuildContext context) {
    return LayoutBuilder(
      builder: (context, constraints) {
        final h = constraints.maxHeight;
        final w = constraints.maxWidth;
        final useFull = h > 18 && w > 64;
        final useHalf = h > 10 && w > 44;

        if (useFull) {
          return _renderLines(_buildLines(_full));
        } else if (useHalf) {
          return _renderLines(_buildLines(_half));
        } else {
          return Text(
            _display,
            style: TextStyle(color: color, fontWeight: FontWeight.bold),
          );
        }
      },
    );
  }

  Component _renderLines(List<String> lines) {
    return Column(
      mainAxisSize: MainAxisSize.min,
      children: [
        for (final line in lines)
          Text(line, style: TextStyle(color: color)),
      ],
    );
  }
}
